    def teach_concept(self, concept: str, user_industry: str = None, depth: str = "full") -> str:
        # OpenClaw 特殊处理
        if any(t in concept.lower() for t in ["openclaw","open claw"]):
            return self._teach_openclaw(concept)
        # 知识图谱游览模式
