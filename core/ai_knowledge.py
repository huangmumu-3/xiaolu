    def teach_concept(self, concept: str, user_industry: str = None, depth: str = "full") -> str:
        key = self._normalize_concept(concept)
        knowledge = CONCEPT_KNOWLEDGE.get(key)
        if not knowledge:
            return self._teach_unknown_concept(concept)
